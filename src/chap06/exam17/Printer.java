package chap06.exam17;
// Exam16과 동일

public class Printer {
	
	static void println(int value) { // 정수형 변수를 받겠다는 것
		System.out.println(value);
	}
	
	static void println(boolean value) { // 불린 타입 변수를 받겠다는 것
		System.out.println(value);
	}
	
	static void println(double value) { // 더블 타입 변수를 받겠다는 것
		System.out.println(value);
	}
	
	static void println(String value) { // 문자열 타입 변수
		System.out.println(value);
	}
}
